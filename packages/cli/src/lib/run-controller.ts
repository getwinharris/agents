import type {
	ConversationStreamChunk,
	DeliveredAttachment,
	BapxClient,
	BapxEvent,
	WorkflowRunResult,
} from '@bapX/sdk';

export interface AgentRunInput {
	message: string;
	images?: DeliveredAttachment[];
}

interface AgentRunTarget {
	kind: 'agent';
	name: string;
	instanceId: string;
	input: AgentRunInput;
}

interface WorkflowRunTarget {
	kind: 'workflow';
	name: string;
	input?: unknown;
}

export type RunTarget = AgentRunTarget | WorkflowRunTarget;

export type RunTargetResult =
	| { kind: 'agent' }
	| { kind: 'workflow'; runId: string; result: unknown };

export async function runTarget(
	client: BapxClient,
	target: RunTarget,
	onEvent?: (event: ConversationStreamChunk | BapxEvent) => void | Promise<void>,
	signal?: AbortSignal,
): Promise<RunTargetResult> {
	if (target.kind === 'agent') {
		const admission = await client.agents.send(target.name, target.instanceId, {
			message: {
				kind: 'user',
				body: target.input.message,
				...(target.input.images?.length ? { attachments: target.input.images } : {}),
			},
			signal,
		});
		await client.agents.wait(admission, { onEvent, signal });
		return { kind: 'agent' };
	}
	const completed: WorkflowRunResult = await client.workflows.run(target.name, {
		input: target.input,
		onEvent,
		signal,
	});
	return { kind: 'workflow', runId: completed.runId, result: completed.result };
}

export function parseAgentInput(value: unknown): AgentRunInput {
	if (!isRecord(value) || typeof value.message !== 'string') {
		throw new TypeError('[bapX] Agent input must be an object with a string "message" field.');
	}
	const keys = Object.keys(value);
	if (keys.some((key) => key !== 'message' && key !== 'images')) {
		throw new TypeError('[bapX] Agent input accepts only "message" and optional "images" fields.');
	}
	if (value.images !== undefined && !isAgentImages(value.images)) {
		throw new TypeError(
			'[bapX] Agent input "images" must be an array of { type: "image", data: string, mimeType: string } objects.',
		);
	}
	return value.images === undefined
		? { message: value.message }
		: { message: value.message, images: value.images };
}

export function assertRunIdAllowed(kind: RunTarget['kind'], id: string | undefined): void {
	if (kind === 'workflow' && id !== undefined) {
		throw new Error('[bapX] --id is not supported for workflows. Workflow run IDs are generated by the runtime.');
	}
}

function isAgentImages(value: unknown): value is DeliveredAttachment[] {
	return (
		Array.isArray(value) &&
		value.every(
			(image) =>
				isRecord(image) &&
				Object.keys(image).length === 3 &&
				image.type === 'image' &&
				typeof image.data === 'string' &&
				typeof image.mimeType === 'string',
		)
	);
}

function isRecord(value: unknown): value is Record<string, unknown> {
	return typeof value === 'object' && value !== null && !Array.isArray(value);
}
